package LV_0.DAY2;

import java.util.Scanner;

public class Solution {
  public static void main(String[] args) {
    Scanner sc = new Scanner(System.in);
    String a = sc.next();

    for (char i : a.toCharArray())
      System.out.println(i);
  }
}